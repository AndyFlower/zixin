package com.cul.culsite.model.response.culapp;

public class PartialCardPresentResponse {

	private String code; // 返回码
	private String msg; // 返回信息
	private String newCardNo; // 新卡的卡号
	private String amount; // 新卡的余额

	public PartialCardPresentResponse() {
		super();
	}

	public PartialCardPresentResponse(String code, String msg,
			String newCardNo, String amount) {
		super();
		this.code = code;
		this.msg = msg;
		this.newCardNo = newCardNo;
		this.amount = amount;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getNewCardNo() {
		return newCardNo;
	}

	public void setNewCardNo(String newCardNo) {
		this.newCardNo = newCardNo;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	@Override
	public String toString() {
		return "PartialCardPresentResponse [code=" + code + ", msg=" + msg
				+ ", newCardNo=" + newCardNo + ", amount=" + amount + "]";
	}

}
