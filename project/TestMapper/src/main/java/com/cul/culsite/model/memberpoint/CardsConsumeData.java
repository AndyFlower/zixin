package com.cul.culsite.model.memberpoint;

public class CardsConsumeData {

	private String cardBalanceAmount;
	
	private String cardNo;
	
	private String expiryDate;
	
	private String cardRedeemedAmount;
	
	private String terminal;
	
	private String cardTxnid;

	public String getCardBalanceAmount() {
		return cardBalanceAmount;
	}

	public void setCardBalanceAmount(String cardBalanceAmount) {
		this.cardBalanceAmount = cardBalanceAmount;
	}

	public String getCardNo() {
		return cardNo;
	}

	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}

	public String getExpiryDate() {
		return expiryDate;
	}

	public void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}

	public String getCardRedeemedAmount() {
		return cardRedeemedAmount;
	}

	public void setCardRedeemedAmount(String cardRedeemedAmount) {
		this.cardRedeemedAmount = cardRedeemedAmount;
	}

	public String getTerminal() {
		return terminal;
	}

	public void setTerminal(String terminal) {
		this.terminal = terminal;
	}

	public String getCardTxnid() {
		return cardTxnid;
	}

	public void setCardTxnid(String cardTxnid) {
		this.cardTxnid = cardTxnid;
	}
	
	
}
