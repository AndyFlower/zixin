import numpy as np;
"""
[[ 1. -1.]
 [-1.  1.]] 负相关 变化方向相反
"""
print(np.corrcoef([1,2,3,4],[4,3,2,1]))
"""
[[ 1.         -0.91350028]
 [-0.91350028  1.        ]] 负相关 变化方向相反
"""
print(np.corrcoef([1,2,3,4],[8,3,2,1]))
"""
[[1. 1.]
 [1. 1.]] 正相关 变化方向一致
"""
print(np.corrcoef([1,2,3,4],[1,2,3,4]))
"""
[[1.        0.8010362]
 [0.8010362 1.       ]] 正相关  变化趋势接近
"""
print(np.corrcoef([1,2,3,4],[1,2,3,40]))